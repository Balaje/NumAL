% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

%My Code
cla
syms x;
func = get(handles.edit1, 'String');
f = inline(func);

str1 = get(handles.edit2, 'String');
str2 = get(handles.edit3, 'String');
str3 = get(handles.edit4, 'String');
str4 = get(handles.edit5, 'String');

x0 = str2double(str1);
x1 = str2double(str2);
x2 = str2double(str3);
tol = str2double(str4);

N=1000;

str = char(32);
for i=1:N
    h1 = x1-x0;
    h2 = x2-x1;
    d1 = (f(x1)-f(x0))/h1;
    d2 = (f(x2)-f(x1))/h2;
    
    a = (d2-d1)/(h1+h2);
    b = a*h2+d2;
    c = f(x2);
    
    D = sqrt(b^2-4*a*c);
    if(b<0)
        E = b-D;
    else
        E = b+D;
    end
    
    x3 = x2 - (2*c/E);
    
    str1 = ['Iteration: ', char(9), num2str(i), char(9), num2str(x3)];
    cmd = [str, char(10), str1, char(10)];
    set(handles.edit6, 'String', cmd);        
    str = [str,char(10),str1];
    
    if(abs(x3-x2) <= tol)
        break;
    end
    x0 = x1;
    x1 = x2;
    x2 = x3;
end

hold on
axis([-10 10 -10 10]);
plot([-10 10],[0 0], 'k-');
plot([0 0], [-10 10], 'k-');
axis([-10 10 -10 10]);
fplot(func,[-3,3]);
hold off


