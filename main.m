str = get(hObject, 'String');
switch str{val};
    case 'Bisection Method'
        handles.rchoice = 1;
    case 'Fixed Point Method'
            handles.rchoice = 2;
    case 'Newton Method'
            handles.rchoice = 3;
    case 'Muller Method'
            handles.rchoice = 4;
    case 'Secant Method'
            handles.rchoice = 5;
    case 'False Position Method'
            handles.rchoice = 6;
end

