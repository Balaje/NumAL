% --- Executes on button press in pushbutton3.
function pushbutton3_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton3 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
close(handles.figure1);
if(handles.roots)
    switch handles.rchoice;
        case 1
            run('bisection.m');
        case 2
            run('fixed.m');
        case 3
            run('newton.m');
        case 4
            run('Muller.m');
        case 5
            run('secant.m');
        case 6
            run('regula.m');
    end
end

if(handles.sys)
    switch handles.schoice;
        case 1
            run('jacobi.m')
        case 2
            run('siedel.m');
        case 3
            fprintf('Newton Method (still under development)');
    end
end

if(handles.inter)
    switch handles.ichoice
        case 1
            run('lagrange.m');
        case 2
            run('divided.m');
        case 3
            run('forward.m');
        case 4 
            run('backward.m');
    end
end

