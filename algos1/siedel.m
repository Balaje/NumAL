% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

syms x;
str1 = get(handles.edit3, 'String');
str2 = get(handles.edit4, 'String');
str3 = get(handles.edit5, 'String');
str4 = get(handles.edit6, 'String');

A = str2num(str1);
B = str2num(str2);
x = str2num(str4);

[m,n] = size(A);
[p,q] = size(B);

if(n ~= q || m ~= n || p ~= 1)
    set(handles.edit1, 'String', 'Invalid system of equations. Check your input');
end

tol = str2double(str3);
y = zeros(1,n);

N=1000;
str = char(32);
for k=1:N
	for i=1:n
		sum=0;
		for j=1:n
			if(j <= i-1)
				sum = sum+A(i,j)*y(j); %Using the most recently calculated value y(j)
			end
			if(j >= i+1)
				sum = sum+A(i,j)*x(j); %Same as the Jacobi method
			end
			y(i) = (-sum+B(i))/A(i,i);
		end
    end
    
    str1 = ['Iteration: ', char(9), num2str(k), char(9), mat2str(y,5)];
    cmd = [str, char(10), str1, char(10)];
    set(handles.edit1, 'String', cmd);        
    str = [str,char(10),str1];
    
	if(norm(y-x,inf) <= tol)
        vpa(y,4);
        return ;
    end
    
	x = y;
end



