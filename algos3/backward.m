% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA);
syms x;
syms s;
if(handles.state1 == 0 && handles.state2 == 0)
    cmd = 'Choose a method before hitting compute';
    set(handles.edit6, 'String', cmd);
end

if(get(handles.radiobutton1, 'Value') == 1)
    str1 = get(handles.edit2, 'String');
    xi = str2num(str1);
    str2 = get(handles.edit3, 'String');
    func = inline(str2);
    
    for i=1:length(xi)
        fxi(i) = double(func(xi(i)));
    end   
end

if(get(handles.radiobutton2, 'Value') == 1)
    str1 = get(handles.edit4, 'String');
    xi = str2num(str1);
    str2 = get(handles.edit5, 'String');
    fxi = str2num(str2);
    
    if(length(fxi) ~= length(xi))
        set(handles.edit6, 'String', 'Array sizes not the same');
    end
end
n = length(xi);

h = xi(2) - xi(1);
for i=2:n-1
  if(xi(i+1)-xi(i) ~= h)
    error('Data Points are not equally spaced');
  end
end

D = zeros(n,n);
D(:,1) = fxi';
for j=2:n
  for k=j:n
      D(k,j) = (D(k,j-1)-D(k-1,j-1))/(xi(k)-xi(k-j+1));
  end
end

sum = D(n,1);
product = 1;
for i=2:n
    product = product*(s+i-2)*h;
    sum = sum + product*D(n,i);
end


s = (x-xi(n))/h;
sum = poly2sym(sym2poly(sum), s);

set(handles.edit1, 'String', ' ');
poly = vpa(collect(sum),4);
str = char(poly);
cmd = ['The interpolating polynomial is',char(10),str];
set(handles.edit1, 'String', cmd);

x = [0:0.25:5];
y = polyval(sym2poly(poly), x);
plot(x,y);




